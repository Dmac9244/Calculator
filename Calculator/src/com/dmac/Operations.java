package com.dmac;

public class Operations {

public Double add(Double num_1, Double num_2){
        return num_1 + num_2;
    }

public Double sub(Double num_1, Double num_2){
    return num_1 - num_2;
}

public Double mul(Double num_1, Double num_2){
    return num_1 * num_2;
}

public Double div(Double num_1, Double num_2){
    return num_1 / num_2;
}
}
